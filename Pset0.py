import math

# Getting the x and y inputs from user
x = int(input("Enter value of x: "))
y = int(input("Enter value of y: "))

# Printing the result of x**y
print(f"x**y = {x**y} ")

# Assigning the value of log(2, x) to the log variable
log = math.log(2, x)

# Printing the result of log(2, x)
print(f"log(x) = {log}")
